_TAG_FIELDS = (
    "IPTC:Keywords",
    "XMP-dc:Subject",
    "XMP-lr:HierarchicalSubject",
    "XMP-digiKam:TagsList",
)


def _build_tag_args(tag: str, operator: str) -> list[str]:
    """Build exiftool args for a single tag. operator is '+=' or '-='."""
    hs = hierarchical_subject(tag)
    return [
        f"-IPTC:Keywords{operator}{tag}",
        f"-XMP-dc:Subject{operator}{tag}",
        f"-XMP-lr:HierarchicalSubject{operator}{hs}",
        f"-XMP-digiKam:TagsList{operator}{tag}",
    ]


def add_tags(path: Path, keywords: list[str], dry_run: bool = False) -> bool:
    """Add tags to a single file using exiftool."""
    if not keywords:
        log.warning("No keywords to write for %s", path.name)
        return False
    if dry_run:
        log.info("[DRY RUN] Would write %d keywords to %s:", len(keywords), path.name)
        for kw in sorted(keywords):
            log.info("    %s", kw)
        return True

    args = ["exiftool", "-overwrite_original"]
    for kw in keywords:
        args.extend(_build_tag_args(kw, "+="))
    args.append(str(path))

    cfg = get_config()
    try:
        result = subprocess.run(args, capture_output=True, text=True,
                                timeout=cfg.exiftool.timeout)
        if result.returncode != 0:
            log.error("exiftool failed for %s: %s", path.name, result.stderr.strip())
            return False
        log.info("Wrote %d keywords to %s", len(keywords), path.name)
        return True
    except FileNotFoundError:
        log.error("exiftool not found. Install with: brew install exiftool")
        sys.exit(1)
    except Exception as e:
        log.error("Failed to write keywords to %s: %s", path.name, e)
        return False


def remove_tags(
    paths: list[Path],
    tags: list[str],
    *,
    also_remove_leaves: bool = False,
    dry_run: bool = False,
) -> bool:
    """Remove tags from one or more files using exiftool.

    Unifies clear_our_tags (autotag), apply_tag_change and bulk_delete_tags (duplicates).
    """
    if not paths or not tags:
        return True
    if dry_run:
        log.info("[DRY RUN] Would remove %d tag(s) from %d file(s)", len(tags), len(paths))
        return True

    cfg = get_config()
    batch_size = cfg.exiftool.batch_size
    success = True

    for i in range(0, len(paths), batch_size):
        batch = paths[i:i + batch_size]
        args = ["exiftool", "-overwrite_original"]
        for tag in tags:
            args.extend(_build_tag_args(tag, "-="))
            if also_remove_leaves:
                leaf = tag.rsplit("/", 1)[-1]
                if leaf != tag:
                    args.append(f"-IPTC:Keywords-={leaf}")
                    args.append(f"-XMP-dc:Subject-={leaf}")
        args.extend(str(f) for f in batch)

        try:
            result = subprocess.run(args, capture_output=True, text=True,
                                    timeout=cfg.exiftool.timeout)
            if result.returncode != 0:
                log.warning("Could not remove tags: %s", result.stderr.strip())
                success = False
        except Exception as e:
            log.warning("Error removing tags: %s", e)
            success = False

    return success


def clear_all_keywords(path: Path, dry_run: bool = False) -> bool:
    """Nuclear option: remove ALL IPTC keywords and XMP subjects from a file."""
    if dry_run:
        log.info("[DRY RUN] Would clear ALL keywords from %s", path.name)
        return True

    args = [
        "exiftool", "-overwrite_original",
        "-IPTC:Keywords=", "-XMP-dc:Subject=",
        "-XMP-lr:HierarchicalSubject=", "-XMP-digiKam:TagsList=",
        str(path),
    ]
    cfg = get_config()
    try:
        result = subprocess.run(args, capture_output=True, text=True,
                                timeout=cfg.exiftool.timeout)
        if result.returncode != 0:
            log.warning("Could not clear keywords from %s: %s", path.name, result.stderr.strip())
            return False
        log.debug("Cleared ALL keywords from %s", path.name)
        return True
    except Exception as e:
        log.warning("Error clearing keywords from %s: %s", path.name, e)
        return False


