# ---------------------------------------------------------------------------
# Tag utilities
# ---------------------------------------------------------------------------

def hierarchical_subject(tag: str) -> str:
    """Convert 'animal/beagle' to 'animal|beagle' for XMP-lr:HierarchicalSubject."""
    return tag.replace("/", "|")


def is_our_tag(tag: str) -> bool:
    """Check if a tag was generated by this tool (has our prefix or is a bare flag)."""
    t = tag.lower()
    if t in BARE_TAGS:
        return True
    for prefix in ALL_PREFIXES:
        if t.startswith(prefix):
            return True
    return False


def deduplicate(tags: list[str]) -> list[str]:
    seen = set()
    deduped = []
    for t in tags:
        if t and t not in seen:
            seen.add(t)
            deduped.append(t)
    return deduped


